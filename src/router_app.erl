-module(router_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    case router_sup:start_link() of
        {error, _} = Error ->
            Error;
        Ok ->
            router_cli_registry:register_cli(),
            Ok
    end.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

